def suma(a, b):
    """
    Función que realizará la operación de suma con los dos valores dados
    :param a:
    :param b:
    :return: el resultado de la suma
    """
    return a + b


def resta(a, b):
    """
        Función que realizará la operación de resta con los dos valores dados
        :param a:
        :param b:
        :return: el resultado de la resta
        """
    return a - b


def multiplication(a, b):
    """
        Función que realizará la operación de multiplicación con los dos valores dados
        :param a:
        :param b:
        :return: el resultado de la multiplicación
        """
    return a * b


def division(a, b):
    """
        Función que realizará la operación de división con los dos valores dados
        :param a:
        :param b:
        :return: el resultado de la división
        """
    return a / b

