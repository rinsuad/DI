def suma(a,b):
    print("La suma de ambos valores es: ", a+b)

def resta(a,b):
    print("La resta del primer valor menos el segundo valor es: ", a-b)

def multiplication(a,b):
    print("La multiplicación es: ", a*b)

def division(a,b):
    print("La división es: ", a/b)

suma(2,3)
resta(2,3)
multiplication(2,5)
division(6,2)